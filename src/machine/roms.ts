import * as base64 from 'base64-js'

export function buildRom(romData: number[]) {
  const byteArray = new Uint8Array(romData)
  const str = base64.fromByteArray(byteArray)
  let start = 0
  while (start < str.length) {
    let end = Math.min(start + 80, str.length)
    const subStr = str.substring(start, end)
    console.log("\'" + subStr + "\',")
    start = end
  }
}

